print('Operacoes com strings')
s = ' Ola mundo '

print('a' + 'b')  # Concatenacao
print('a' * 3)  # Repeticao
print(s[1])  # Indexacao
print(s[1:3])  # Fatiamento
print("Ol" in s)  # Verifica se a substring esta contida na string
print("Ol" not in s)  # Verifica se a substring nao esta contida na string
print(s.upper())  # Upper case
print(s.lower())  # Lower case
print(s.capitalize())  # Capitalize
print(len(s))  # Tamanho da string
print('=' * 30)
